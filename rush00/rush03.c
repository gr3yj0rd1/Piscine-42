#include <unistd.h>

void	rush(int x, int y)
{
	int	w;
	int	l;

	w = 0;
	while (w < y)
	{
		l = 0;
		while (l < x)
		{
			if ((l == 0) && (w == 0 || w == y - 1))
				write (1, "A", 1);
			else if ((l == x - 1) && (w == 0 || w == y - 1))
				write (1, "C", 1);
			else if ((l == 0 || l == x - 1) && (w != 0 || w != 1))
				write (1, "B", 1);
			else if ((w == 0 || w == y - 1) && (l != 0 || l != x - 1))
				write (1, "B", 1);
			else
				write (1, " ", 1);
			l++;
		}
		write (1, "\n", 1);
		w++;
	}
}
